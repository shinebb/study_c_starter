#include <stdio.h>


int main(void)
{
	int x = 10;
	int y = 20;
	printf("x = %d입니다.\n", x);
	printf("y = %d입니다.\n", y);
	printf("x + y = %d입니다.\n", x + y);
	printf("x - y = %d입니다.\n", x - y);
	printf("x * y = %d입니다.\n", x * y);
	printf("x / y = %d입니다.\n", x / y); //몫만 나옴(나머지 제외)
	printf("x % y = %d입니다.\n", x % y); //나머지만 나옴 
	return 0;
} 
